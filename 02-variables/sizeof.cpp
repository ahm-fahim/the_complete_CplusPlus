#include <iostream>
using namespace std;

int main(){
    int number = 10;

    cout << "sizeof num: " << sizeof(number) << endl;
    cout << "sizeof int: " << sizeof(int) << endl;
    cout << "sizeof string: " << sizeof(string) << endl;
    cout << "sizeof double: " << sizeof(double) << endl;
    cout << "sizeof float: " << sizeof(float) << endl;
}