#include <iostream>
using namespace std;

int main()
{
    int num1{10}, num2{10};

    cout << "num1 < num2: " << (num1 < num2) << endl;
    cout << "num1 > num2: " << (num1 > num2) << endl;
    cout << "num1 <= num2: " << (num1 <= num2) << endl;
    cout << "num1 >= num2: " << (num1 >= num2) << endl;
    cout << "num1 != num2: " << (num1 != num2) << endl;
    cout << "num1 == num2: " << (num1 == num2) << endl;
}